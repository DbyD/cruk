							<h3>What is "Our Heroes"?</h3>
							The "Our Heroes: Extraordinary People, Extraordinary Effort" scheme gives all staff the opportunity to recognise the great work done by colleagues every day. You can recognise any peer, manager or report across the charity, with a simple thanks online or by nominating someone for a 'Little Extra' award to acknowledge extra special effort. In addition, an annual award ceremony takes place each December, recognising the best of the best and celebrating our top heroes who have really pushed the boundaries of their role to help beat cancer sooner. </li>
						<li><a name="b"></a>
							<h3>What form does the recognition take?</h3>
							There are three forms of recognition, ranging from a simple 'thank you' for a job well done, to an award with a 'Little Extra', when your nominee has done something beyond expectations, to a nomination for an Annual Our Heroes Award, for exceptional and outstanding performance.
							<h4>A simple 'thank you'</h4>
							Nominees will receive an e-card or, for those colleagues without a work email address, a printed congratulatory card and their contribution will be posted on the Our Heroes site 'Wall of Fame'.
							<h4>An award with a 'Little Extra'</h4>
							In addition to a card, nominees receive a 'Little Extra' to accompany their award. This means that your colleague will receive a choice of voucher, experience and/or work-based reward, which they can redeem through the Our Heroes site.
							<h4>An Annual Award </h4>
							The pan-charity annual award ceremony takes place in December. This is a chance to celebrate and recognise those who have shown outstanding performance and deserve an extra special form of recognition. There is a submission window each autumn for Annual Award nominations. </li>
						<li><a name="c"></a>
							<h3>How do I nominate?</h3>
							<h4>A simple 'thank you' or award with a 'Little Extra'</h4>
							Log onto the "Our Heroes" site and visit the 'Nominate' page, where you can follow the process to nominate either an individual or team of your choice.
							<p>You will need to nominate colleagues against one of our three belief categories and write why their contribution makes them worthy of an award. Once submitted, all contributions will be posted on the Our Heroes site 'Wall of Fame' and will be shared with the individual / team members you nominate and their manager(s). </p>
							<p>To add a 'Little Extra' award, select the button in the nominate page. You'll need to explain why you think the nominee deserves this special level of recognition. Your nomination will be sent for approval by the person's Head or Senior Manager.</p>
							<h4>An Annual Award </h4>
							There is a submission window each autumn for Annual Award nominations. The process is straightforward:
							<ul>
								<li>Visit the 'Annual Our Heroes Award' page on the Our Heroes site</li>
								<li>Enter your details and the person or team you want to nominate</li>
								<li>Nominate them against one of the annual award categories</li>
								<li>Write a clear example showing why you think that person/team deserves to be nominated for an Annual Our Heroes Award.</li>
								<li>Really sell them! Remember that the staff panel who are reading this nomination may not know this person or team, or what they've done, so you need to spell out why they are fantastic. Overall what exactly makes them stand out? How do they really live the brand? Keep this in mind when you are writing your nomination.</li>
								<li>Click submit!</li>
							</ul>
							Once the nomination deadline has passed, a volunteer staff panel will review each of the nominations and select the tops nominations, which are deserving of an Annual Our Heroes Extraordinary People Extraordinary Effort Award.
							<p>Winners receive a special tailored Annual Our Heroes award from an Executive Board member when the ceremony takes place.</p>
						</li>

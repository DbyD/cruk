							<h3>Can I nominate someone in the year round programme <em>and</em> for an Annual Our Heroes Award? </h3>
							Yes you can. </li>
						<li><a name="e"></a>
							<h3>Who can I nominate?</h3>
							You can nominate as many individuals or teams as you like, at any grade and in any role or directorate. Unfortunately, this recognition scheme does not include volunteers, interns, casual workers or contractors. If you would like to nominate a volunteer, then visit the 'Flame of Hope' intranet page to find out more.   </li>
						<li><a name="f"></a>
							<h3>Can I nominate my manager?</h3>
							Yes, you can recognise and nominate any member of staff across the charity regardless of grade, team or directorate. </li>
						<li><a name="g"></a>
							<h3>Can I donate my 'Little Extra' award back to Cancer Research UK?</h3>
							Unfortunately, it is not possible for us to donate awards back to the charity. The administration involved would not be cost efficient for the charity and it also causes issues from a financial auditing perspective.
							<p>However, if you don't wish to accept your 'Little Extra' award, you can email the ourheroes@cancer.org.uk email address, to let us know.</p>
						</li>
						<li><a name="h"></a>
							<h3>Why does the Head or Senior Manager of the person I want to recognise need to authorise a nomination with an additional award attached?</h3>
							Heads or Senior Managers authorise all nominations with a 'Little Extra' award, so that they know who is being recognised in their team and can ensure that there are no exceptional circumstances that might make it inappropriate to offer the award. </li>
						<li><a name="i"></a>
							<h3>What if my Head or Senior Manager doesn't approve a nomination for a 'Little Extra' award?</h3>
							In the very unlikely event that this does happen, we will send the nominator a message to explain why the award has been declined. </li>
						<li><a name="j"></a>
							<h3>Where does the money come from?</h3>
							There is a central budget which has been approved by the Executive Board to be used to recognise the extraordinary effort of our staff. </li>
